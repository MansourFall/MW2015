	
	Thank you for your interest in supporting <span class="highlight-extra-pages">MY World - the United Nations global survey for a better world.</span>
	<br><br>
	From now until 2015, we want as many people in as many countries as possible to be involved with MY World:<span class="highlight-extra-pages"> citizens of all ages, genders and backgrounds.</span>
	<br><br>
	Here you can find resources (below) to help you spread the word about MY World and directly <span class="highlight-extra-pages">engage people from your community.</span>
	<br><br>
	
	The support of a variety of <span class="highlight-extra-pages">groups and individuals</span> is vital in reaching out directly into communities and drawing the digitally disconnected, illiterate and poorest communities into the global debate. 
	<br/>
