		<!---------------------------- ###########   HOW TO GET INVOLVED  #############-------------------------------->
							  <li style="margin-left:0">
							  	<span  class="highlight-extra-pages resource-header" ">How can you get involved with MY World?</span><br><br>
							  </li>
							
								<li>
								    <span class="highlight-extra-pages">Spread the word online</span> by using our online engagement guide, to help you tweet, email, post and blog about MY World.For resources, see below.
							    <br><br>
							    </li>
							    
							    <li>
							    	<span class="highlight-extra-pages">Embed the MY World ballot</span> on your website using the mini ballot. Everything you need, including how to embed the ballot, 
							    	can be found here: <a href="http://partner.myworld2015.org/widget.html" class="highlighted-link">http://partner.myworld2015.org/widget.html</a>
							    
							    <br><br>
							    </li>
							    
							    <li>
							        <span class="highlight-extra-pages">Administer the survey offline</span> in local communities by distributing ballot cards, gathering results and uploading the data. 
							        This can be organized through a school, a community group, faith group or work place. For resources, see below. 	
							    <br><br>
							    </li>
							    
							    <li>
							      <span class="highlight-extra-pages">Download the MY World Android App</span> and use it to collect votes without the need for printing or data entry. 
							      Search <a href="https://play.google.com/store/search?q=MYWorld2015" class="highlighted-link">Google Play</a> for myworld2015 and use your partner ID to start collecting votes. 
							    
							    <br><br>
							    </li>
							    
							    
							    <li>
							     <span class="highlight-extra-pages">Include MY World </span> at your events by distributing ballot papers among the participants,
							      inviting them to take the survey online, and discussing the sixteen options. 
							    <br><br>
							    </li>
							    
							    
							    
							    <li>
							    <span class="highlight-extra-pages">Reach out to your partners, local organizations and members</span> and encourage them to participate.
							     MY World is open to all. 
							    <br><br>
							    </li>
							    
							    
							    
							    <li>
								<span class="highlight-extra-pages">Engage with other potential partners</span> for example, mobile phone providers and media outlets, to facilitate the reach and impact of MY World.
							    <br><br>
							    </li>
							    
							    
							    <li>
								<span class="highlight-extra-pages">Help drive traffic </span> to <a href="http://www.myworld2015.org" class="highlighted-link">www.myworld2015.org </a>through external networks and communications platforms (websites, news bulletins and social media activities).
							    <br><br>
							    </li>
							    
							    <li>
							    <span class="highlight-extra-pages">Send news!</span> Collect photos, videos and stories of those engaging in MY World; 
							    re-tweet @myworld2015 hashtag #post2015 and share on Facebook at MYWorld2015 to join the conversation on social media 
								<br>
							    <br>
								</li>
